    if(!imu_init())
    {
        zf_log(0, "IMU init failed.");
        while(true);
    }

    // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ

